export const textColor = (num: number) => {
  if (num === 1) {
    return 'blue';
  }
  if (num === 2) {
    return 'green';
  }
  if (num === 3) {
    return 'red';
  }
  if (num === 4) {
    return 'indigo';
  }
  if (num === 5) {
    return 'coral';
  }
  if (num === 6) {
    return 'deepPink';
  }
  if (num === 7) {
    return 'purple';
  }
  if (num === 8) {
    return 'black';
  }
};
